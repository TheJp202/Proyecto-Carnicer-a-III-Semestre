    void BuscarCarne(){
     int id = Integer.parseInt(txtCodCarne.getText());
         if (txtCodCarne.getText().equals("")) {
             JOptionPane.showMessageDialog(null, "Debe ingresar el codigo de la carne");
         }else{
             ca = car.ListarID(id);
             if (ca.getIdcarne()!=0) {
                 txtNombreCarne.setText(ca.getDescrip());
                 txtcodcate.setText(""+ca.getCodcategoria());
                 txtcodanimal.setText(""+ca.getCodanimal());
                 txtFechavenc.setText(ca.getFechVenc());
                 txtPeso.setText(""+ca.getPeso());
                 txtcodmarca.setText(""+ca.getCodMarca());
                 txtPrecio.setText(""+ca.getPrecioTotal());
                 txtPrecioUni.setText(""+ca.getPrecioUnidad());
                 txtcodprov.setText(""+ca.getIdpro());
                 txtcantidad.setText(""+ca.getCantidad());
                 
                 
             }else{
             JOptionPane.showMessageDialog(null, "Carne no registrada");
             txtCodCarne.requestFocus();
             }
         }
     }
